# PigGame
2020/09/23
1、最基础的实现了购猪，分猪，卖猪的功能，其中包括自动售猪和随机买猪等全自动功能。
2、可以具体查看各猪圈，圈中各猪的信息。
3、用qt完成了简洁的用户界面

2020/09/27
1、添加了猪瘟系统，从三十天开始便有一定几率使猪圈中的某头猪感染猪瘟，此后每天都会有可能感染给本猪圈及附近猪圈的猪
注意！猪瘟存在预感染，即感染后第一天不会表现出症状，也不会被侦测到。
2、完善了金钱系统。增加游戏目标：赚钱到两百万元。
每次购猪都会花钱，而隔离猪瘟也要花钱（虽然政府会补偿少量损失），请合理使用隔离功能！
3、增加存档系统，即用文件来保存当前信息，下次开始若继续游戏则会读取当前信息，新开游戏就会清空。
4、增加查看某品种猪整体数量与体重，数量与饲养时间的关系图像的功能。
5、添加了背景音乐
6、完善整个ui，更加舒服
